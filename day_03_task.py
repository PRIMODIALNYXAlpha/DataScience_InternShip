"""# Task 05: File Data Counter
# Requirements:
# function to count:
# - lines
# - words
# - characters
"""

def file_data_counter(filename):
    try:
        with open(filename, "r", encoding="utf-8") as file:
            text = file.read()

        lines = text.split("\n")
        words = text.split()
        characters = len(text)

        return len(lines), len(words), characters

    except FileNotFoundError:
        return None, None, None


