"""# Task 04: Employee Salary Calculator
# Requirements:
# - hra = 20%
# - pf = 12%
# return net salary
"""

def net_salary(basic_salary):
    hra = basic_salary * 0.20
    pf = basic_salary * 0.12

    gross_salary = basic_salary + hra
    net = gross_salary - pf

    return net


print("Task 04: Employee Salary Calculator")

basic_salary = 40000
final_salary = net_salary(basic_salary)

print("Basic Salary:", basic_salary)
print("Net Salary:", final_salary)

