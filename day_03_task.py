"""# Task 02: ATM Withdrawal Function
# Function: withdraw(balance, amount)
# Check:
# - amount <= balance
# - amount is multiple of 100
# Return updated balance
"""

def withdraw(balance, amount):
    if amount <= 0:
        return balance, "Invalid withdrawal amount ❌"

    if amount > balance:
        return balance, "Insufficient balance ❌"

    if amount % 100 != 0:
        return balance, "Amount must be a multiple of 100 ❌"

    balance -= amount
    return balance, "Withdrawal successful ✅"


print("Task 02: ATM Withdrawal Function")

balance = 5000
amount = 1200

new_balance, message = withdraw(balance, amount)

print("Old Balance:", balance)
print("Withdraw Amount:", amount)
print(message)
print("New Balance:", new_balance)

print("\n" + "-"*60 + "\n")

"""# Task 03: Password Validator
# Requirements:
# - min 8 characters
# - at least 1 digit
# - at least 1 uppercase letter
"""

def validate_password(password):
    if len(password) < 8:
        return False, "Password must be at least 8 characters ❌"

    has_digit = False
    has_upper = False

    for ch in password:
        if ch.isdigit():
            has_digit = True
        if ch.isupper():
            has_upper = True

    if not has_digit:
        return False, "Password must contain at least 1 digit ❌"
    if not has_upper:
        return False, "Password must contain at least 1 uppercase letter ❌"

    return True, "Password is valid ✅"


